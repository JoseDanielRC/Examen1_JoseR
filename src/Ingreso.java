    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (jt_libros.getSelectedRow() >= 0) {
            DefaultTableModel modelo = (DefaultTableModel) jt_libros.getModel();
            modelo.removeRow(jt_libros.getSelectedRow());
            jt_libros.setModel(modelo);
        }
        for (int i = 0; i < libros.size(); i++) {
            if (tf_eliminar.getText().equals(libros.get(i).getTitulo())) {
                libros.remove(i);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

