    private void EntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EntrarMouseClicked
        tp_administar.setEnabled(false);
        if (usuario.equals(tf_usuario.getText()) && contrasena.equals(tf_contraseña.getText())) {
            tp_administar.setEnabled(true);
            JOptionPane.showMessageDialog(this, "Bienvenido Administrador");

        } else {
            for (int i = 0; i < usuarios.size() + 1; i++) {
                if (usuarios.get(i).getUsuario().equals(tf_usuario.getText()) && usuarios.get(i).getContraseña().equals(tf_contraseña.getText())) {
                    JOptionPane.showMessageDialog(this, "Usted ha ingresado con exito");

                }
            }

        }
        {

        }
    }//GEN-LAST:event_EntrarMouseClicked

