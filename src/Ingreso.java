    private void b_registrarseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_registrarseMouseClicked
        JOptionPane.showMessageDialog(this, "Usted ha sido registrado con exito");
        String genero = "";
        if (rb_fantasia.isSelected()) {
            genero = "Fantasia";
        } else if (rb_romance.isSelected()) {
            genero = "Romance";
        } else if (rb_accion.isSelected()) {
            genero = "Accion";
        } else if (rb_historia.isSelected()) {
            genero = "Historia";
        }
        usuarios.add(new Usuario(tf_nuevousuario.getText(), tf_nuevacontra.getText(), tf_nuevotel.getText(), genero));
    }//GEN-LAST:event_b_registrarseMouseClicked

    private void jb_buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_buscarMouseClicked

        for (int i = 0; i < libros.size(); i++) {
            if (tf_titulo.getText().equals(libros.get(i).getTitulo())) {
                Object[] newrow = {
                    libros.get(i).getTitulo(), libros.get(i).getAutor(), libros.get(i).getGenero(), libros.get(i).getPuntaje(),libros.get(i).getValor()};
                        DefaultTableModel modelo = (DefaultTableModel) jt_libros.getModel();
            modelo.addRow(newrow);
            jt_libros.setModel(modelo);
            } else if (tf_titulo.getText().equals("")) {
                Object[] newrow = {
                    libros.get(i).getTitulo(), libros.get(i).getAutor(), libros.get(i).getGenero(), libros.get(i).getPuntaje(),libros.get(i).getValor()};
                 DefaultTableModel modelo = (DefaultTableModel) jt_libros.getModel();
            modelo.addRow(newrow);
            jt_libros.setModel(modelo);
            } else {
                JOptionPane.showMessageDialog(this, "No se encuentra en disposicion");
            }
        }
    }//GEN-LAST:event_jb_buscarMouseClicked

